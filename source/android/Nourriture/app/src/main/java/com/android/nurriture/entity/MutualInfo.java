public class MutualInfo {
    private String ingreName;
    private String otherIngre;
    private String reason;

    public String getIngreName() {
        return ingreName;
    }

    public void setIngreName(String ingreName) {
        this.ingreName = ingreName;
    }

    public String getOtherIngre() {
        return otherIngre;
    }

    public void setOtherIngre(String otherIngre) {
        this.otherIngre = otherIngre;
    }

    public String getReason() {
        return reason;
    }

    public void setReason(String reason) {
        this.reason = reason;
    }
}
